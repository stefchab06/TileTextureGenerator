namespace TileTextureGenerator.Core.Enums;

/// <summary>
/// Sides of a rectangular image in clockwise order.
/// </summary>
public enum ImageSide
{
    /// <summary>
    /// Top edge of the image.
    /// </summary>
    Top = 0,

    /// <summary>
    /// Right edge of the image.
    /// </summary>
    Right = 1,

    /// <summary>
    /// Bottom edge of the image.
    /// </summary>
    Bottom = 2,

    /// <summary>
    /// Left edge of the image.
    /// </summary>
    Left = 3
}
